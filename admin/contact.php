function getRow(id){
  $.ajax({
    type: 'POST',
    url: 'contact_row.php',
    data: {id:id},
    dataType: 'json',
    success: function(response){
      $('.name').html(response.prodname);
      $('.prodid').val(response.prodid);
      $('#edit_name').val(response.prodname);

      CKEDITOR.instances["editor2"].setData(response.description);
     getCategory();getSubCategory();
    }
  });
} 

